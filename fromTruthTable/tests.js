import {Test} from '../misc/test';
import {getExpr} from './simplifyExpr';

[
  ['00', 'F'],
  ['01', 'A'],
  ['10', '!A'],
  ['11', 'T'],
  ['0000', 'F'],
  ['0001', '(A&B)'],
  ['0010', '(A&!B)'],
  ['0011', 'A'],
  ['0100', '(!A&B)'],
  ['0101', 'B'],
  ['0110', '(!A&B)|(A&!B)'],
  ['0111', 'B|A'],
  ['1000', '(!A&!B)'],
  ['1001', '(!A&!B)|(A&B)'],
  ['1010', '!B'],
  ['1011', '!B|A'],
  ['1100', '!A'],
  ['1101', '!A|B'],
  ['1110', '!A|!B'],
  ['1111', 'T'],
  ['00000000', 'F'],
  ['11111111', 'T'],
  ['11110000', '!A'],
  ['11001100', '!B'],
  ['10101010', '!C'],
  ['1101000111010001', '(!B&!C)|(C&D)'],
].forEach(([input, expected]) => {
  Test.assertEquals(getExpr(input), expected, input);
});
