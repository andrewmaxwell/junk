function normalize(shape: Shape): Shape {
  const width = Math.max(...shape.map((r) => r.length));
  const result: Shape = [];
  for (let i = 0; i < shape.length; i++) {
    result[i] = [];
    for (let j = 0; j < width; j++) {
      result[i][j] = shape[i][j] ? 1 : 0;
    }
  }
  while (result[0].every((v) => v === 0)) result.shift(); // trim top
  while (result[result.length - 1].every((v) => v === 0)) result.pop(); // trim bottom
  while (result.every((r) => r[0] === 0)) result.forEach((r) => r.shift()); // trim left
  while (result.every((r) => r[r.length - 1] === 0))
    result.forEach((r) => r.pop()); // trim right
  return result;
}

function rotate(shape: Shape): Shape {
  const result: Shape = [];
  for (let i = 0; i < shape.length; i++) {
    for (let j = 0; j < shape[i].length; j++) {
      result[j] ??= new Array<number>(shape.length).fill(0);
      result[j][shape.length - i - 1] = shape[i][j];
    }
  }
  return normalize(result);
}

function flip(shape: Shape): Shape {
  return normalize(shape).map((r) => r.toReversed());
}

function getRotations(shape: Shape): Shape[] {
  const r0 = normalize(shape);
  const r90 = rotate(shape);
  const r180 = rotate(r90);
  const r270 = rotate(r180);
  const f0 = flip(r0);
  const f90 = rotate(f0);
  const f180 = rotate(f90);
  const f270 = rotate(f180);
  const seen = new Set();
  return [r0, r90, r180, r270, f0, f90, f180, f270].filter((a) => {
    const json = JSON.stringify(a);
    return !seen.has(json) && seen.add(json);
  });
}

function isValid(shape: Shape, board: Shape, x: number, y: number) {
  for (let i = 0; i < shape.length; i++) {
    for (let j = 0; j < shape[i].length; j++) {
      if (shape[i][j] && !board[i + y]?.[j + x]) return false;
    }
  }
  return true;
}

type Offset = {x: number; y: number};
function getPositions(shape: Shape, board: Shape): Offset[] {
  const boardWidth = Math.max(...board.map((r) => r.length));
  const shapeWidth = shape[0].length;
  const result: Offset[] = [];
  for (let y = 0; y < board.length - shape.length + 1; y++) {
    for (let x = 0; x < boardWidth - shapeWidth + 1; x++) {
      if (isValid(shape, board, x, y)) result.push({x, y});
    }
  }
  return result;
}

function getConfig(board: Shape, shapes: Record<string, Shape>) {
  const config: string[][] = [];
  for (const s in shapes) {
    for (const r of getRotations(shapes[s])) {
      const positions = getPositions(r, board);
      for (const p of positions) {
        const cells: string[] = [];
        // each row
        for (let i = 0; i < r.length; i++) {
          // each col
          for (let j = 0; j < r[i].length; j++) {
            // ignore 0 cells
            if (!r[i][j]) continue;
            cells.push(`${p.x + j}_${p.y + i}`); // cells are just `x_y`
          }
        }
        config.push([s, ...cells]);
      }
    }
  }
  return config;
}

