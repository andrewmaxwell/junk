function findSolution(m) {
  return [
    ...m[0]
      .map((b, i) => [b, i])
      .filter(([b]) => !b)
      .map(([, i]) => m.length + i),
    ...m
      .map((r, j) => [r[0], j])
      .filter(([b, j]) => b ^ m[0][0])
      .map(([_, j]) => j),
  ];
}

