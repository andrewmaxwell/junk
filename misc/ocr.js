const toGrid = ({pixels, width, height}) => {
  const result = [];
  for (let i = 0; i < height; i++) {
    result[i] = pixels.slice(i * width, (i + 1) * width);
  }
  return result;
};

// normalizes values and rounds them to 0 or 1
const contrast = (grid) => {
  let min = Infinity;
  let max = -Infinity;
  for (const row of grid) {
    for (const v of row) {
      min = Math.min(min, v);
      max = Math.max(max, v);
    }
  }
  return grid.map((r) => r.map((v) => Math.round((v - min) / (max - min))));
};

const rotate = (grid) => {
  const result = [];
  for (let i = 0; i < grid[0].length; i++) {
    result[i] = [];
    for (let j = 0; j < grid.length; j++) {
      result[i][j] = grid[grid.length - 1 - j][i];
    }
  }
  return result;
};

// looks for empty rows in a grid and splits it up
const splitChars = (grid) => {
  const result = [];
  let prevHasBlack = true;
  for (const row of grid) {
    const hasBlack = row.some((x) => !x);
    if (hasBlack) {
      if (!prevHasBlack) result.push([]);
      result[result.length - 1].push(row);
    }
    prevHasBlack = hasBlack;
  }
  return result;
};

const findLastIndex = (arr, pred) => {
  for (let i = arr.length - 1; i >= i; i--) {
    if (pred(arr[i], i, arr)) return i;
  }
  return -1;
};

// removes blank space around the edges of a grid
const trim = (grid) => {
  const top = grid.findIndex((r) => r.some((v) => !v));
  const bottom = findLastIndex(grid, (r) => r.some((v) => !v));
  const left = grid[0].findIndex((c, i) => grid.some((r) => !r[i]));
  const right = findLastIndex(grid[0], (c, i) => grid.some((r) => !r[i]));
  return grid.slice(top, bottom + 1).map((r) => r.slice(left, right + 1));
};

// resizes an array (smaller only!) with interpolation
const interpolate = (arr, newLen) => {
  const result = Array(newLen).fill(0);
  const oldLen = arr.length;
  for (let i = 0; i < oldLen * newLen; i++) {
    result[Math.floor(i / oldLen)] += arr[Math.floor(i / newLen)] / oldLen;
  }
  return result;
};

// takes a grid and resizes it (smaller only!), interpolating the values
// the returned grid is rotated, because who cares?
const resize = (grid, h = 3, w = 5) =>
  rotate(grid.map((r) => interpolate(r, w))).map((r) => interpolate(r, h));

// takes two arrays of numbers and returns how different they are.
const calcDiff = (a, b) =>
  [...a].reduce((total, val, i) => total + Math.abs(val - b[i]), 0);

